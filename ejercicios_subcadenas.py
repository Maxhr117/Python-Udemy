# 1- Extraer los primeros 5 caracteres
cadena1 = 'Programacion en Python'
print(cadena1[0:5])

# 2- Obtener los ultimos 4 caracteres
cadena2 = 'Aprendiendo slicing'
print(cadena2[-4:])

# 3- Obtener del caracter 3 al 8
cadena3 = 'Lenguaje'
print(cadena3[2:6])

# 4- Invertir una cadena
cadena4 = 'Python'
print(cadena4[::-1])

# 5- Mostrar solo los caracteres en posiciones pares
cadena5 = 'Subcadena'
print(cadena5[::2])
# 6- Extraer una palabra de una frase
cadena6 = 'Hola mundo desde Python'  # extrae solo la palabra "desde"
print(cadena6[11:16])
