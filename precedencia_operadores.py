# Tabla de precedencia(de mayor a menor poder)
# Nivel	    Operador	            Significado	                               Ejemplo
# 1	            ()	            Paréntesis(siempre lo primero)	            (3 + 4) * 2 → 14
# 2	            **	             Potencia	                                2 ** 3 → 8
# 3	        -x, +x, ~x	         Unarios (negativo, positivo, bitwise)	        -5
# 4	        *, /, //, %	        Multiplicación, división, módulo	        10 % 3 → 1
# 5	            +, -	        Suma y resta	                            3 + 2 → 5
# 6	        <, >, <=, >=	    Comparaciones	                            5 > 3 → True
# 7	           ==, !=	       Igualdad / desigualdad	                    4 == 4 → True
# 8	            not	            Negación lógica	                            not True → False
# 9	            and	            Y lógico	                                True and False
# 10	        or	            O lógico	                                True or False
