# operadores_aritmeticos
# suma, concatenar +
a = 45
b = 100
suma = a + b
print(suma)  # a + b = 3
# resta -
resta = a - b
print(resta)
# multiplicacion *
multiplicacion = 5 * 4
print(multiplicacion)
# division
division = 35 / 10
print(division)
# Division entera
division_entera = 100 // 10
print(division_entera)
# modulo (el restante de la division)
modulo = 7 % 3
print(modulo)
# potencia(exponente)
potencia = 3 ** 3
print(potencia)
